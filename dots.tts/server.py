_TEMP_AUDIO_DIR = Path("/tmp/dots_tts_rest_api")
_TEMP_AUDIO_DIR.mkdir(exist_ok=True)


def _write_temp_wav(raw_bytes: bytes) -> str:
    """
    Write raw audio bytes to a temporary WAV file.

    The input is expected to be a valid WAV file (just bytes).  We write it
    directly to disk because the runtime expects a file path.
    """
    import uuid

    path = _TEMP_AUDIO_DIR / f"{uuid.uuid4().hex}.wav"
    path.write_bytes(raw_bytes)
    logger.debug("Wrote temporary prompt audio: {}", path)
    return str(path)


def _cleanup_temp(path: str) -> None:
    """Remove a temporary audio file if it exists."""
    try:
        Path(path).unlink(missing_ok=True)
    except OSError:
        pass


def _tensor_to_wav_bytes(
    audio_tensor: torch.Tensor, sample_rate: int
) -> bytes:
    """
    Convert a torch audio tensor to WAV-encoded bytes.

    Parameters
    ----------
    audio_tensor : torch.Tensor
        Shape (1, samples), dtype float32 in [-1, 1].
    sample_rate : int
        Sample rate in Hz.

    Returns
    -------
    bytes
        Raw WAV file bytes.
    """
    buffer = io.BytesIO()
    sf.write(
        buffer,
        audio_tensor.float().cpu().squeeze().numpy(),
        sample_rate,
        format="WAV",
        subtype="PCM_16",
    )
    return buffer.getvalue()


