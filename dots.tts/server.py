_runtime: DotsTtsRuntime | None = None


def _get_runtime() -> DotsTtsRuntime:
    """Return the global runtime, loading it once if necessary."""
    global _runtime
    if _runtime is None:
        logger.info(
            "Loading dots.tts model '{}' (precision={}) ...",
            MODEL_NAME_OR_PATH,
            DEFAULT_PRECISION,
        )
        _runtime = DotsTtsRuntime.from_pretrained(
            MODEL_NAME_OR_PATH,
            precision=DEFAULT_PRECISION,
        )
        logger.info("Model loaded successfully.")
    return _runtime


